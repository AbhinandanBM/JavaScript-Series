// While

let index = 0
while (index <= 20) {
    // console.log(`value of index: ${index}`);
    index = index+2
}

let myArr = ["virat","Rohith","Dhoni","ABD"]

let arr = 0
while (arr < myArr.length) {
    // console.log(`Value of array: ${myArr[arr]}`);
    arr++
}


/****************************** do while ************************** */ 

let score = 12

do {
    console.log(`Score is: ${score}`);
    score++
} while (score <= 10);